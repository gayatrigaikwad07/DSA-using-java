public class equal_operator {
    public static void main(String[] args) {
        int A = 10;
        int B = 10;
        System.out.println(A == B);
        System.out.println(A != B);
        System.out.println(A > B);
        System.out.println(A >= B);
        System.out.println(A < B);
        System.out.println(A <= B);
    
    }
    
}
